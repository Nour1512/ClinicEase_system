class Appointment:
    def __init__(self, name, phone, email, date, time, area, city, state, post_code):
        self.name = name
        self.phone = phone
        self.email = email
        self.date = date
        self.time = time
        self.area = area
        self.city = city
        self.state = state
        self.post_code = post_code